public class Control01 {

	public static void main(String[] args) {
		
		int age = 40;
		
		if(age <= 39) {
			if(19 <= age) {
			System.out.println
			("19세 이상 39세 이하인 사람은 청년저축 가입 가능합니다.");
			}
			}
		
		else {
				System.out.println
				("39세 이상인 사람은 청년 저축 가입 불가능합니다. ");
			}
		
		System.out.println("프로그램 종료.");
		
	}
}
